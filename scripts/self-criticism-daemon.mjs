#!/usr/bin/env node
// Self-criticism daemon — continuously analyzes the grant detection and
// extraction pipelines, identifies weaknesses, proposes improvements.
// This daemon is the system's quality conscience: it watches HOW we find and
// extract grants, criticizes the approach, and suggests concrete improvements
// to reach perfection. It runs less frequently (~60 min) since deep analysis
// is expensive, but its criticism is permanent evidence.
//
// Usage: node scripts/self-criticism-daemon.mjs [intervalMinutes=60]

import { execSync } from "node:child_process";
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { join } from "node:path";
import { logTo, ollamaChatWhenIdle, stamp, registerDaemon } from "./daemon-shared.mjs";

const INTERVAL_MIN = Number(process.argv[2]) || 60;
const LOG_FILE = "scripts/self-criticism-report.log";
const FINDINGS_FILE = "scripts/self-criticism-findings.md";
const AUDIT_LOG = "scripts/live-audit-report.log";
const METRICS_FILE = "scripts/self-eval-metrics.jsonl";

const log = (section, message) => logTo(LOG_FILE, section, message);

// Recent audit failures: stuck grants, low grounding, duplicates, etc.
function getAuditFailures() {
  try {
    if (!existsSync(AUDIT_LOG)) return "";
    const lines = readFileSync(AUDIT_LOG, "utf8").trim().split("\n").filter(Boolean);
    const failures = lines.filter(
      (l) =>
        /(stuck at max|DUPLICATE|IMPLAUSIBLE|FABRICATED|enrich.*fail)/i.test(l) &&
        !/findings for/i.test(l),
    );
    return failures.slice(-30).join("\n");
  } catch {
    return "";
  }
}

// Latest scorecard metrics: what's broken.
function getLatestMetrics() {
  try {
    if (!existsSync(METRICS_FILE)) return null;
    const lines = readFileSync(METRICS_FILE, "utf8").trim().split("\n").filter(Boolean);
    if (!lines.length) return null;
    return JSON.parse(lines[lines.length - 1]);
  } catch {
    return null;
  }
}

// Read detection/extraction code snippets to criticize.
function readPipelineCode() {
  const snippets = {};
  const files = [
    "src/lib/grant-detection.ts",
    "src/lib/grant-extraction.ts",
    "src/lib/grant-enrichment.ts",
    "src/server/grant-ingestion.ts",
  ];
  for (const f of files) {
    try {
      if (existsSync(f)) {
        const code = readFileSync(f, "utf8");
        // Take first 1000 chars of comments/docstring + 2000 chars of active code
        const lines = code.split("\n").slice(0, 50).join("\n");
        snippets[f] = lines;
      }
    } catch {
      // ignore
    }
  }
  return snippets;
}

async function cycle() {
  log("cycle", "--- analyzing grant detection & extraction pipelines ---");

  const metrics = getLatestMetrics();
  const auditFailures = getAuditFailures();
  const pipelineCode = readPipelineCode();

  const analysis = {
    cycle_timestamp: stamp(),
    metrics: metrics
      ? {
          grounding: metrics.grounding_coverage_pct,
          completeness: metrics.data_completeness_pct,
          duplicates: metrics.duplicate_clusters,
          stuck_grants: metrics.stuck_at_max_attempts,
          fit_median: metrics.fit_median,
        }
      : null,
    recent_failures: auditFailures || "[no failures logged]",
    pipeline_files: Object.keys(pipelineCode),
  };

  const result = await ollamaChatWhenIdle(
    "qwen2.5:7b",
    [
      {
        role: "system",
        content:
          "You are a relentless code reviewer and architect, analyzing a grant-intelligence system. " +
          "Your job: be a CRITIC. You are given metrics (what's broken), recent audit failures (where we fail), " +
          "and code snippets (how we try). " +
          "Identify 3-5 REAL WEAKNESSES in the grant detection/extraction pipelines. " +
          "Each weakness must cite: (1) the broken metric or failure log line, (2) the root cause in the code/logic, " +
          "(3) a concrete, implementable fix. " +
          "CRITICIZE: regex too loose, validation missing, edge case unhandled, assumption wrong, design flawed. " +
          "Do NOT propose vague advice. Do NOT list multiple issues in one item. " +
          "Format: [WEAKNESS] <title>, [METRIC] <what fails>, [ROOT] <code/logic issue>, [FIX] <concrete action>. " +
          "If the system is perfect, reply: [NONE] No actionable weaknesses detected — pipelines are sound.",
      },
      {
        role: "user",
        content: JSON.stringify(analysis, null, 2),
      },
    ],
    { timeoutMs: 320000, numCtx: 8192, numPredict: 800, holder: "self-criticism" },
  );

  if (result.skipped) {
    log("analysis", `skipped (${result.reason}) - will retry next cycle`);
    log("cycle", `--- done, sleeping ${INTERVAL_MIN}m ---`);
    return;
  }

  const body =
    `# Self-criticism findings\n\n` +
    `_Auto-generated by self-criticism-daemon.mjs at ${stamp()} using local qwen2.5:7b.\n` +
    `This daemon is the system's conscience: it criticizes the grant detection & extraction ` +
    `pipelines to identify weaknesses and propose improvements toward perfection._\n\n` +
    `${result.text.trim()}\n`;

  writeFileSync(FINDINGS_FILE, body);
  log("criticism", `wrote ${FINDINGS_FILE}`);
  log("cycle", `--- done, sleeping ${INTERVAL_MIN}m ---`);
}

async function main() {
  await registerDaemon("self-criticism");
  log("daemon", `self-criticism-daemon started, polling every ${INTERVAL_MIN} minutes`);
  while (true) {
    await cycle().catch((e) => log("cycle", `FATAL (continuing): ${e.message}`));
    await new Promise((r) => setTimeout(r, INTERVAL_MIN * 60 * 1000));
  }
}

main();
